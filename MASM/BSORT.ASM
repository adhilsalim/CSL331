ASSUME CS: CODE, DS: DATA

DATA SEGMENT
    ARRAY DW 1111H, 5555H, 4444H, 2222H, 3333H
    LEN EQU($-ARRAY)
DATA ENDS

CODE SEGMENT
START:
    MOV AX, DATA
    MOV DS, AX

    MOV CH, LEN
    LEA SI, ARRAY

OUTLOOP:
    MOV CL, LEN
    LEA SI, ARRAY

INLOOP:
    MOV AX, [SI]
    MOV BX, [SI+2]
    CMP AX, BX

    JL NEXT

    MOV [SI], BX
    MOV [SI+2], AX

NEXT:
    INC SI
    INC SI
    DEC CL
    JNZ INLOOP
    DEC CH
    JNZ OUTLOOP

STOP:
    MOV AH, 4CH
    INT 21H
CODE ENDS
END START